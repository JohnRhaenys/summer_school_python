# bool
x = False
y = True
"""
O Python considera True como 1 e False como 0
"""
# Adicao
print('Adicao: ', x + y)

# Subtracao
print('Subtracao: ', x - y)

# Multiplicacao
print('Multiplicacao: ', x * y)

# Divisao
print('Divisao matematica: ', x / y)

# Divisao (inteira)
# Exemplo: Quero distribuir 587 computadores para 8 escolas, igualmente. Quantos computadores
# cada escola vai receber? Quantos computadores vao sobrar?
print('Divisao inteira: ', x // y)

# Resto da divisao
print('Resto da divisao: ', x % y)

# Valor absoluto
print('Valor absoluto (modulo): ', abs(y))

# Potencia
print('x elevado a y: ', x ** y)
