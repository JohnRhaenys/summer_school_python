# floats
x = 1.0
y = 3.0

# Adicao
print('Adicao: ', x + y)

# Subtracao
print('Subtracao: ', x - y)

# Multiplicacao
print('Multiplicacao: ', x * y)

# Divisao (igual na matematica)
print('Divisao matematica: ', x / y)

# Divisao ("inteira")
print('Divisao inteira: ', x // y)
print('Tipo da divisao inteira: ', type(x//y))

# Resto da divisao
print('Resto da divisao: ', x % y)

# Absolute value
print('Valor absoluto (modulo): ', abs(y))

# Potencia
print('x elevado a y: ', x ** y)
