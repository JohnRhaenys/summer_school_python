""" Inserindo par chave/valor no dicionario """

dicionario = {}

print(dicionario)

# Aqui estou inserindo um par chave/valor, em que a chave eh a string 'Harry' e o valor
# associado a ela eh o numero 5
dicionario['Harry'] = 5
print(dicionario)

# Aqui estou alterando o valor de uma chave ja existente. Neste caso, eu estou
# sobrescrevendo o valor relacionado `a chave 'Harry'
dicionario['Harry'] = 10
